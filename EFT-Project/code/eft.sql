drop table if exists Classes;
create table Classes(
    c_class varchar(32) PRIMARY KEY,
    c_total integer,
    c_num integer
);

drop table if exists Ammunition;
create table Ammunition(
    a_caliber varchar(32),
    a_ammunition varchar(32)
);

drop table if exists Weapons;
create table Weapons(
    w_class integer,
    w_name varchar(32),
    w_firingMode varchar(32),
    w_fireRate varchar(32),
    w_caliber integer
);

drop table if exists Armor;
create table Armor(
    arm_name varchar(32),
    arm_class integer,
    arm_penType integer,
    arm_zones varchar(32),
    arm_durability integer,
    arm_movement_speed varchar(32),
    arm_turn_speed varchar(32),
    arm_ergo integer
);

drop table if exists Backpack;
create table Backpack(
    b_class integer,
    b_name varchar(32),
    b_innerGrid varchar(32),
    b_outerGrid varchar(32),
    b_totalSlots integer,
    b_storageEfficiency integer,
    b_weight integer
);

drop table if exists GearMods;
create table GearMods(
    g_class integer,
    g_name varchar(32),
    g_recoil integer,
    g_ergo integer
);

drop table if exists VitalParts;
create table VitalParts(
    v_class integer,
    v_name varchar(32),
    v_recoil integer,
    v_ergo integer,
    v_accuracy integer,
    v_muzzleVelocity integer
);

drop table if exists Sights;
create table Sights(
    s_class integer,
    s_name varchar(32),
    s_recoil integer,
    s_ergo integer,
    s_accuracy integer,
    s_sightingRange integer,
    s_magnification varchar(32)
);

drop table if exists Magazine;
create table Magazine(
    m_class integer,
    m_name varchar(32),
    m_ergo integer,
    m_checkSpeed integer,
    m_loadSpeed integer,
    m_capacity varchar(32),
    m_caliber varchar(32)
);

